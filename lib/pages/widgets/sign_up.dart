  // Function to handle sign-up with email and password
  void _toggleSignUpButton() async {
    String email = signupEmailController.text;
    String password = signupPasswordController.text;
    String confirmPassword = signupConfirmPasswordController.text;

    if (email.isEmpty || password.isEmpty || confirmPassword.isEmpty) {
      CustomSnackBar(context, const Text('Please fill all fields'));
      return;
    }

    if (password != confirmPassword) {
      CustomSnackBar(context, const Text('Passwords do not match'));
      return;
    }

    try {
      UserCredential userCredential = await FirebaseAuth.instance
          .createUserWithEmailAndPassword(
          email: email, password: password);
      widget.onSignUpSuccess(); // On successful sign-up
    } catch (e) {
      CustomSnackBar(context, Text(e.toString()));
    }
  }

  // Function to handle Google sign-up
  Future<void> _signUpWithGoogle() async {
    try {
      GoogleSignInAccount? googleUser = await _googleSignIn.signIn();
      GoogleSignInAuthentication? googleAuth =
      await googleUser?.authentication;

      OAuthCredential credential = GoogleAuthProvider.credential(
        accessToken: googleAuth?.accessToken,
        idToken: googleAuth?.idToken,
      );

      UserCredential userCredential =
      await FirebaseAuth.instance.signInWithCredential(credential);

      widget.onSignUpSuccess(); // On successful sign-up
    } catch (e) {
      CustomSnackBar(context, Text(e.toString()));
    }
  }

  void _toggleSignup() {
    setState(() {
      _obscureTextPassword = !_obscureTextPassword;
    });
  }

  void _toggleSignupConfirm() {
    setState(() {
      _obscureTextConfirmPassword = !_obscureTextConfirmPassword;
    });
  }
}
